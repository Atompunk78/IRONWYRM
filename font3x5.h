static inline int Font3x5GlyphIndex(char character) {
    if(character >= '0' && character <= '9') {
        return character - '0';
    }

    if(character >= 'A' && character <= 'Z') {
        return 10 + character - 'A';
    }

    if(character >= 'a' && character <= 'z') {
        return 10 + character - 'a';
    }

    switch((unsigned char)character) {
    case ':': return 36;
    case ',': return 37;
    case '-': return 38;
    case '!': return 39;
    case 0xA3: return 40;
    case '.': return 41;
    case '?': return 42;
    case '(': return 43;
    case ')': return 44;
    case '\'': return 45;
    case '\xA2': return 46;
    default: return -1;
    }
}
